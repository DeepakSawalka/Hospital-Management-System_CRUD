    private void selectbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectbtnActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex=hospitaldoctordetailstbl.getSelectedRow();
        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Data not Found");
             return;}
             
          DefaultTableModel model=(DefaultTableModel) hospitaldoctordetailstbl.getModel();
          HospitalDoctor selectedHospitalDoctor=(HospitalDoctor ) model.getValueAt(selectedRowIndex, 0);
          
         
          AppointmentJFrame a=new AppointmentJFrame();
          a.hospitalnamelbl.setText(selectedHospitalDoctor.getHospitalname());
          a.hospitaladdresslbl.setText(selectedHospitalDoctor.getHospitaladdress());
          a.zipcodelbl.setText(String.valueOf(selectedHospitalDoctor.getZipcode()));
          a.citylbl.setText(selectedHospitalDoctor.getCity());
          a.doctornamelbl.setText(selectedHospitalDoctor.getDoctorname());
          a.genderlbl.setText(selectedHospitalDoctor.getGender());
          a.emailidlbl.setText(selectedHospitalDoctor.getEmailid());
          a.contactnolbl.setText(String.valueOf(selectedHospitalDoctor.getContactno()));
          a.degreelbl.setText(selectedHospitalDoctor.getDegree());
          a.specialitylbl.setText(selectedHospitalDoctor.getSpeciality());
          a.experiencelbl.setText(selectedHospitalDoctor.getExperience());
          a.availabilitylbl.setText(selectedHospitalDoctor.getAvailability());
          
          a.setVisible(true);
          
          
          
          
          
      
    }//GEN-LAST:event_selectbtnActionPerformed

