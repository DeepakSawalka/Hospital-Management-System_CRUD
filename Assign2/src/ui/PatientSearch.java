private void populateTableHd() {
        DefaultTableModel model=(DefaultTableModel) hospitaldoctordetailstbl.getModel();
        model.setRowCount(0);
        
        for (HospitalDoctor hd : doctorHospitalHistory.getDoctorHospitalHistory())
        {
            Object[] col=new Object[12];
            col[0]= hd;
            col[1]= hd.getHospitaladdress();
            col[2]= hd.getZipcode();
            col[3]= hd.getCity();
            col[4]= hd.getDoctorname();
            col[5]= hd.getGender();
            col[6]= hd.getEmailid();
            col[7]= hd.getContactno();
            col[8]= hd.getDegree();
            col[9]= hd.getSpeciality();
            col[10]= hd.getExperience();
            col[11]= hd.getAvailability();
             
            
            model.addRow(col);
            
        }
}



