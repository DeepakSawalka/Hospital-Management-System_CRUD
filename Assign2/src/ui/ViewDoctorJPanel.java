    private void viewdoctordetailstblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_viewdoctordetailstblMouseClicked
        // TODO add your handling code here:
        
        int selectedRowIndex=viewdoctordetailstbl.getSelectedRow();
        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Data not Found");
             return;}
             
          DefaultTableModel model=(DefaultTableModel) viewdoctordetailstbl.getModel();
          Doctor selectedDoctor=(Doctor) model.getValueAt(selectedRowIndex, 0);
          
          namevdoctxt.setText(selectedDoctor.getName());
          gendervdoctxt.setText(selectedDoctor.getGender());
          emailidvdoctxt.setText(selectedDoctor.getEmailid());
          contactnovdoctxt.setText(String.valueOf(selectedDoctor.getContactno()));
          degreevdoctxt.setText(selectedDoctor.getDegree());
          specialityvdoctxt.setText(selectedDoctor.getSpeciality());
          experiencevdoctxt.setText(selectedDoctor.getExperience());
          
          
    }//GEN-LAST:event_viewdoctordetailstblMouseClicked

    private void deletevdocbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletevdocbtnActionPerformed
        // TODO add your handling code here:
         int i=viewdoctordetailstbl.getSelectedRow();
        if (i<0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
             return;}
             
          DefaultTableModel model=(DefaultTableModel) viewdoctordetailstbl.getModel();
          Doctor selectedDoctor=(Doctor) model.getValueAt(i, 0);
          
          doctorHistory.deleteDoctor(selectedDoctor);
          JOptionPane.showMessageDialog(this, "Doctor Records Deleted Successfully!!");
          populateTableVDoc();
         namevdoctxt.setText("");
        gendervdoctxt.setText("");
        emailidvdoctxt.setText("");
        contactnovdoctxt.setText("");
        degreevdoctxt.setText("");
        specialityvdoctxt.setText("");
        experiencevdoctxt.setText("");
    }//GEN-LAST:event_deletevdocbtnActionPerformed


