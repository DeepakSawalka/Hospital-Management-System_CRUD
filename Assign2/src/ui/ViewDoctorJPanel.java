    private void populateTableVDoc() {
        DefaultTableModel model=(DefaultTableModel) viewdoctordetailstbl.getModel();
        model.setRowCount(0);
        
        for (Doctor d  : doctorHistory.getDoctorHistory())
        {
            Object[] col=new Object[7];
            col[0]= d;
            col[1]= d.getGender();
            col[2]= d.getEmailid();
            col[3]= d.getContactno();
            col[4]= d.getDegree();
            col[5]= d.getSpeciality();
            col[6]= d.getExperience();
            
           
                   
            model.addRow(col);
            
        }
}






